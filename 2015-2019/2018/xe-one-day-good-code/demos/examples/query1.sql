	UPPER(COALESCE(LTRIM(ISNULL(NULLIF(SOG.DSCOGNOME,''),'') + ' ' + SOG.DSNOME),PNA.DSPNA,'')) as Nome,
	COALESCE(PNA.DTCONTPNA,PNA.DTEFFPNA,TES.DTFC) as Datainc,
	COALESCE(PNA.DTEFFPNA,PNA.DTCONTPNA,TES.DTFC) as Dataeff,
	CASE ISNULL(PNA.CDMOPAG,'') WHEN 'S' THEN 'A' WHEN 'E' THEN 'B' WHEN 'C' THEN 'C' WHEN 'A' THEN 'A' WHEN 'T' THEN 'T' ELSE '' END as Mezzopag,
