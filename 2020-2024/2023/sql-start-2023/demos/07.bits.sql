------------------------------------------------------------------------
-- LEFT_SHIFT/RIGHT_SHIFT
------------------------------------------------------------------------
-- 12345  = 0011000000111001
-- 395040 = 01100000011100100000
SELECT LEFT_SHIFT(12345, 5) AS Result;
SELECT 12345 << 5 AS Result;
GO
-- 12345  =      0011000000111001
--   385  = 0000000110000001
SELECT RIGHT_SHIFT(12345, 5) AS Result;
SELECT 12345 >> 5 AS Result;
GO
------------------------------------------------------------------------
-- BIT_COUNT
------------------------------------------------------------------------
SELECT BIT_COUNT(17) as NumBits; -- 17 = 00010001
GO

------------------------------------------------------------------------
-- SET_BIT/GET_BIT (zero-based offset)
------------------------------------------------------------------------
-- 0x00 = 0000 -> 0100 = 0x04
SELECT SET_BIT(0x00, 2, 1) as Result;
GO
-- 0x00 = 0000 -> 0001 = 0x01
SELECT SET_BIT(0x00, 0, 1) as Result;
GO
-- 0xabcdef = 101010111100110111101111
--                               43210
--                               ^ ^
SELECT
	GET_BIT(0xabcdef, 2) as Get_2nd_Bit
,	GET_BIT(0xabcdef, 4) as Get_4th_Bit;
GO
